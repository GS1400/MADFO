% TriSubPoint generates a random triangle subspace point
%
function xt = TriSubPoint(x1,x2,x3)
alpha = randn(3,1);
alpha = alpha/norm(alpha,inf);
xss   = [x1,x2,x3];
for ii=1:3
dX(:,ii) = alpha(ii)*(xss(:,ii));
end
xt = sum(dX')'; 
